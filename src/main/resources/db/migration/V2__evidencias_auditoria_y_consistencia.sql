    accion VARCHAR(100) NOT NULL,
    entidad VARCHAR(80) NOT NULL,
    id_entidad VARCHAR(80) NOT NULL,
    fecha TIMESTAMP NOT NULL,
    resultado VARCHAR(40) NOT NULL,
    informacion_adicional VARCHAR(1000)
);

