CREATE TABLE IF NOT EXISTS evidencias (
    id_evidencia BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_meta_contrato INTEGER NOT NULL,
    numero_intento INTEGER NOT NULL,
    nombre_original VARCHAR(255) NOT NULL,
    tipo_mime VARCHAR(100) NOT NULL,
    tamanio_bytes BIGINT NOT NULL,
    hash_sha256 VARCHAR(64) NOT NULL,
    comentario_deportista VARCHAR(1000),
    estado VARCHAR(30) NOT NULL,
    motivo_rechazo VARCHAR(1000),
    fecha_carga TIMESTAMP NOT NULL,
    fecha_revision TIMESTAMP,
    id_usuario_revisor INTEGER,
    fecha_actualizacion TIMESTAMP NOT NULL,
    version BIGINT NOT NULL DEFAULT 0,
    CONSTRAINT ck_evidencia_tamanio_positivo CHECK (tamanio_bytes > 0),
    CONSTRAINT ck_evidencia_intento_positivo CHECK (numero_intento > 0),
    CONSTRAINT ck_evidencia_estado CHECK (estado IN ('EN_REVISION', 'APROBADA', 'RECHAZADA')),
    CONSTRAINT uq_evidencia_meta_intento UNIQUE (id_meta_contrato, numero_intento)
);

